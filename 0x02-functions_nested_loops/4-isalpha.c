	int _isalpha(int c)
	{
			if (c > 65)
            {
                return (1);
            }
            else if (c > 97)
            {
                return (1);
            }
            else if (c < 90)
            {
                return (1);
            }
            else if (c < 122)
            {
                return (1);
            }
            else
            {
                return (0);
            }
	}

