#include "main.h"


	/**
	 * print_alphabet_x10 - prints 10 times the alphabet, in lowercase,
	 * followed by a new line
	 */

	void jack_bauer(void);
	{
			int i;
			int j;
			int m;
			int n;
			for (i = 0; i < 3; i++)
            {
                for (j = 0; j < 4; j++)
                {
                    for (m = 0; m < 6; m++)
                    {
                        for (n = 0; n < 10; n++)
                        {
                            _putchar(i);
                            _putchar(j);
                            _putchar(':');
                            _putchar(m);
                            _putchar(n);
                            _putchar('\n');
                        }
                    }
                }
            }
	}

